class Calculator {
    public  int add(int number, int numberTwo) {
        return (number + numberTwo);
    }

    public int subtract(int number, int numberTwo){
        return (number - numberTwo);
    }

    public int multiply(int number, int numberTwo){
        return (number * numberTwo);
    }

    public double divide(double number, double numberTwo){
        return (number / numberTwo);
    }
}


